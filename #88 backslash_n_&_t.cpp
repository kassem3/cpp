// \n & \t
/* \n for going to next line
   \t for leaving a space equal to tab key
Note: Unlike endl func. these only work within 
quotations i.e. won't work outside " " */	

#include<iostream>
using namespace std;

int main()
{
	cout<<"Master\nChef"<<endl; //don't forget endl!
	
	cout<<"Gordon\tRamsey";
	
	return 0;
}

